package mBean;

public class MobSubReportBean {
	private String timeBy;
	private String valueBy;
	private String subReportId;

	public String getTimeBy() {
		return timeBy;
	}

	public void setTimeBy(String timeBy) {
		this.timeBy = timeBy;
	}

	public String getValueBy() {
		return valueBy;
	}

	public void setValueBy(String valueBy) {
		this.valueBy = valueBy;
	}

	public String getSubReportId() {
		return subReportId;
	}

	public void setSubReportId(String subReportId) {
		this.subReportId = subReportId;
	}
}
